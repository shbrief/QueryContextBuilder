"""## Method 2: Contextualized Token Extraction (no FAISS)

Reference.md §2: instead of pooling the full sentence, locate the target term's token positions in the hidden states and average *only* those vectors. Because BERT is bidirectional, those token vectors are already context-aware.

**Setup**:
- Corpus: **all rows** from `rag_disease` SQLite (same coverage as FAISS index), embedded with batched forward passes
- Corpus embeddings cached to `data/outputs/corpus_m2_{model}.npy` — reloaded on subsequent runs
- Query: `f"{term} -> {parse_context(context, DISEASE_CTX_KEYS)}"` (term starts at position 0)
- Similarity: in-memory cosine (no FAISS)
"""

