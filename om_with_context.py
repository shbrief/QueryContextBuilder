MAX_LEN_M2 = 256   # truncation length for Method 2

def get_term_span_embedding(
    model, tokenizer, text: str, term: str, device: str = 'cpu'
) -> np.ndarray:
    """
    Embed `text` with `model`, then return the average of the hidden-state
    vectors at positions corresponding to `term`'s tokens.
    Falls back to the [CLS] vector when the term is not found in the text.
    """
    enc = tokenizer(
        text,
        return_tensors='pt',
        truncation=True,
        max_length=MAX_LEN_M2,
        return_offsets_mapping=True,
    )
    offset_mapping = enc.pop('offset_mapping')[0].tolist()   # (seq_len, 2)

    # Locate term's character span in the (possibly truncated) text
    term_start = text.find(term)
    term_end   = term_start + len(term) if term_start != -1 else -1

    if term_start == -1:
        term_token_idx = [0]   # fallback: CLS
    else:
        term_token_idx = [
            i for i, (s, e) in enumerate(offset_mapping)
            if e > term_start and s < term_end and e > s
        ] or [0]

    enc = {k: v.to(device) for k, v in enc.items()}
    with torch.no_grad():
        out    = model(**enc)
        hidden = out.last_hidden_state[0]   # (seq_len, dim)

    vec   = hidden[term_token_idx].mean(dim=0).cpu().numpy().astype('float32')
    norm  = np.linalg.norm(vec)
    return vec / (norm + 1e-12)


def cosine_search_m2(
    query_vec: np.ndarray, corpus_vecs: np.ndarray,
    corpus_meta: list[dict], k: int = TOP_K
) -> list[dict]:
    """Brute-force cosine similarity against a pre-built corpus matrix."""
    q  = query_vec / (np.linalg.norm(query_vec) + 1e-12)
    scores = corpus_vecs @ q          # (N,)
    top_idx = np.argsort(scores)[::-1][:k]
    return [
        {**corpus_meta[i], 'score': float(scores[i])}
        for i in top_idx
    ]

