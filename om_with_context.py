from tqdm.auto import tqdm

BATCH_SIZE_M2  = 32
CORPUS_CACHE   = os.path.join(args.output_dir, f'corpus_m2_{MODEL_METHOD}.npy')  # reuse across runs

def embed_corpus_batched(
    model, tokenizer, texts: list[str], terms: list[str],
    batch_size: int = BATCH_SIZE_M2, device: str = 'cpu'
) -> np.ndarray:
    """Batch-encode corpus; extract term-span hidden states for each entry."""
    dim  = model.config.hidden_size
    vecs = np.zeros((len(texts), dim), dtype='float32')

    for start in tqdm(range(0, len(texts), batch_size), desc='Embedding corpus (M2)'):
        batch_texts = texts[start : start + batch_size]
        batch_terms = terms[start : start + batch_size]

        enc = tokenizer(
            batch_texts,
            return_tensors='pt',
            truncation=True,
            max_length=MAX_LEN_M2,
            padding=True,
            return_offsets_mapping=True,
        )
        offset_maps = enc.pop('offset_mapping').tolist()   # (B, seq_len, 2)
        enc = {k: v.to(device) for k, v in enc.items()}

        with torch.no_grad():
            hidden = model(**enc).last_hidden_state   # (B, seq_len, dim)

        for j, (term, offsets) in enumerate(zip(batch_terms, offset_maps)):
            text       = batch_texts[j]
            term_start = text.find(term)
            term_end   = term_start + len(term) if term_start != -1 else -1

            if term_start == -1:
                idx = [0]
            else:
                idx = [
                    k for k, (s, e) in enumerate(offsets)
                    if e > term_start and s < term_end and e > s
                ] or [0]

            vec  = hidden[j][idx].mean(dim=0).cpu().numpy().astype('float32')
            norm = np.linalg.norm(vec)
            vecs[start + j] = vec / (norm + 1e-12)

    return vecs


