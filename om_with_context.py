def parse_context(ctx_str: str, keys: list[str]) -> str:
    """Extract relevant fields from semicolon key:value context string."""
    result = []
    for part in ctx_str.split(';'):
        part = part.strip()
        if ':' in part:
            k, _, v = part.partition(':')
            if k.strip().upper() in keys:
                result.append(f"{k.strip()}: {v.strip()}")
    return '; '.join(result) if result else ctx_str[:200]

