"""## 3. Define embedding strategies"""

def normalize(v: np.ndarray) -> np.ndarray:
    v = v.astype('float32')
    norms = np.linalg.norm(v, axis=-1, keepdims=True)
    return v / (norms + 1e-12)

def search(query_vec: np.ndarray, k: int = TOP_K) -> list[dict]:
    """Search the existing rag_disease FAISS index with a pre-built query vector."""
    q = normalize(query_vec.reshape(1, -1))
    D, I = vs.index.search(q, k)
    results = []
    for db_idx, score in zip(I[0], D[0]):
        if db_idx == -1:
            continue
        record_id = vs._ids[db_idx]
        row = vs.cursor.execute(
            'SELECT term, code FROM rag_disease WHERE id = ?', (record_id,)
        ).fetchone()
        if row:
            results.append({'term': row[0], 'code': row[1], 'score': float(score)})
    return results


# --- Strategy 0: Baseline (term only) ---
def embed_baseline(term: str, context: str) -> np.ndarray:
    return np.array(embedder.embed_query(term), dtype='float32')


# --- Strategy 1: Prompt concat (Reference.md §1) ---
# Format: "term -> relevant_context_fields"
def embed_concat(term: str, context: str) -> np.ndarray:
    ctx = parse_context(context, DISEASE_CTX_KEYS)
    prompt = f"{term} -> {ctx}"
    return np.array(embedder.embed_query(prompt), dtype='float32')


# --- Strategy 2: Hybrid average (Reference.md §3) ---
# average of embed(term) and embed(relevant_context)
def embed_hybrid(term: str, context: str) -> np.ndarray:
    ctx = parse_context(context, DISEASE_CTX_KEYS)
    v_term = np.array(embedder.embed_query(term), dtype='float32')
    v_ctx  = np.array(embedder.embed_query(ctx),  dtype='float32')
    return normalize((v_term + v_ctx) / 2)


